    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String strItem = txtEntry.getText().trim();
        if(!strItem.isEmpty()){
            clientHelper.add(txtEntry.getText().trim());
            txtEntry.setText("");
        }   
        else JOptionPane.showMessageDialog(this,"Textfield Empty.");
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        String strItem = txtEntry.getText().trim();
        if(!strItem.isEmpty()){//entered text then clicked remove
            clientHelper.remove(txtEntry.getText().trim());
            txtEntry.setText("");
        }else if(! jList1.isSelectionEmpty()){//selected an item in list then clicked remove
            clientHelper.remove(jList1.getSelectedValue().trim());
        }else{
            JOptionPane.showMessageDialog(this,"Please enter an item in the Text Field \nor select an item from the list");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

